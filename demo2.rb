def yellow_page (phone_string) 
    puts phone_string
    name_string = ""
    phone_string.downcase.each_char do |letter|
        if letter = "a" || letter = "b" || letter = "c"
            name_string += '2'
        elsif letter = "d" || letter = "e" || letter = "f"
            name_string += '2'
        elsif letter = "g" || letter = "h" || letter = "i"
            name_string += '2'
        elsif
        if letter = "j" || letter = "k" || letter = "l"
            name_string += '2'
        end
        if letter = "m" || letter = "n" || letter = "o"
            name_string += '2'
        end
        if letter = "p" || letter = "q" || letter = "r"
            name_string += '2'
        end
        if letter = "s" || letter = "t" || letter = "u"
            name_string += '2'
        end
    end
end


yellow_page("test")